function troca()
{
    var x = document.getElementById('rdbCodBarras').checked;

    if (x === true) {
        document.getElementById('txtCodBarras').disabled = false;
        document.getElementById('txtNumSerie').disabled = true;
        document.getElementById('txtCodBarras').focus();
    } else {
        document.getElementById('txtCodBarras').disabled = true;
        document.getElementById('txtNumSerie').disabled = false;
        document.getElementById('txtNumSerie').focus();
    }
}

